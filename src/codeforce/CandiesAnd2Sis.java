package codeforce;

import java.util.*;
import java.io.*;

public class CandiesAnd2Sis {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t=sc.nextInt();
        while(t-->0)
        {
            int n=sc.nextInt();
            System.out.println((n-1)/2);
        }
    }
}
